package basicClass;

public class Historico {
	private int hist_Id;
	private String hist_Data_Registro;
	private String hist_Descricao;
	
	public int cha_Id;
	public int user_Id;
	
	
	public int getHist_Id() {
		return hist_Id;
	}
	public void setHist_Id(int hist_Id) {
		this.hist_Id = hist_Id;
	}
	public String getHist_Data_Registro() {
		return hist_Data_Registro;
	}
	public void setHist_Data_Registro(String hist_Data_Registro) {
		this.hist_Data_Registro = hist_Data_Registro;
	}
	public String getHist_Descricao() {
		return hist_Descricao;
	}
	public void setHist_Descricao(String hist_Descricao) {
		this.hist_Descricao = hist_Descricao;
	}
}
